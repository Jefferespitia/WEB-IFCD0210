
// Declaración de una función

function make() {
    const x = 22;
    console.log(x);
}

// Asignación de una expresión funcional

const foo = function () {
    const x = 22;
    console.log(x);
};

// Asignación de una arrow function

const baz = () => {
    const x = 22;
    console.log(x);
};


make()
foo()
baz()